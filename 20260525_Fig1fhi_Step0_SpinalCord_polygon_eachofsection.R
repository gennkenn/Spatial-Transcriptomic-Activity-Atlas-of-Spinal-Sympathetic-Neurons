# The following code is used to scale the CtrlF, ColdF, 2DGF, and 2DGM data where the ROI has been enclosed by .png data.
library(geojsonsf)

library(geojsonsf)
library(sf)

# geojson フォルダ
base_dir <- "./Xenium_SC/ROI_geojson/ctrlF"

# geojson ファイル一覧
files <- list.files(
  path = base_dir,
  pattern = "\\.geojson$",
  full.names = TRUE
)

# region map
region_map <- c(
  "1" = "T1_4",
  "2" = "T5_7",
  "3" = "T8_10",
  "4" = "T11_13",
  "5" = "L1_3",
  "6" = "L45",
  "7" = "L6S1",
  "8" = "S2_4"
)

# scale
x_scale <- 32.557
y_scale <- 32.1209

for (f in files) {
  
  fname <- tools::file_path_sans_ext(
    basename(f)
  )
  
  parts <- strsplit(fname, "-")[[1]]
  
  animal_num <- parts[1]
  region_num <- parts[2]
  roi_num <- parts[3]
  
  region_name <- region_map[region_num]
  
  obj_name <- paste0(
    "Polygon_CtrlF_N",
    animal_num,
    "_",
    region_name,
    "_",
    roi_num
  )
  
  # =========================
  # read
  # =========================
  
  obj <- geojson_sf(f)
  
  # =========================
  # scale geometry
  # =========================
  
  geom <- st_geometry(obj)
  
  geom_scaled <- lapply(geom, function(g){
    
    # POLYGON
    if (inherits(g, "POLYGON")) {
      
      rings <- lapply(g, function(coords){
        
        coords[,1] <- coords[,1] * x_scale
        coords[,2] <- coords[,2] * y_scale
        
        coords
      })
      
      st_polygon(rings)
      
      # MULTIPOLYGON
    } else if (inherits(g, "MULTIPOLYGON")) {
      
      polys <- lapply(g, function(poly){
        
        lapply(poly, function(coords){
          
          coords[,1] <- coords[,1] * x_scale
          coords[,2] <- coords[,2] * y_scale
          
          coords
        })
      })
      
      st_multipolygon(polys)
      
    } else {
      
      stop("Unsupported geometry type")
    }
  })
  
  st_geometry(obj) <- st_sfc(geom_scaled)
  
  # =========================
  # save
  # =========================
  
  assign(
    obj_name,
    obj,
    envir = .GlobalEnv
  )
  
  cat("Loaded + scaled:", obj_name, "\n")
}



